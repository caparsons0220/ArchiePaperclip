function objectSchema(properties: Record<string, unknown>, required: string[] = []) {
  return {
    type: "object",
    additionalProperties: false,
    properties,
    required,
  };
}

function stringProperty(description: string, extra: Record<string, unknown> = {}) {
  return { type: "string", description, ...extra };
}

function numberProperty(description: string, extra: Record<string, unknown> = {}) {
  return { type: "number", description, ...extra };
}

function booleanProperty(description: string) {
  return { type: "boolean", description };
}

function activeCompanySelector(referenceHint = "Uses the active company supplied by the server.") {
  return {
    entityType: "company",
    scope: ACTIVE_COMPANY_HOME_TOOL_SCOPE,
    fields: [],
    acceptsCanonicalId: false,
    acceptsHumanRef: false,
    referenceHint,
  } satisfies HomeActionSelectorModel;
}

function idOrRefSelector(input: {
  entityType: string;
  idField: string;
  refField: string;
  referenceHint: string;
}) {
  return {
    entityType: input.entityType,
    scope: ACTIVE_COMPANY_HOME_TOOL_SCOPE,
    fields: [input.idField, input.refField],
    acceptsCanonicalId: true,
    acceptsHumanRef: true,
    referenceHint: input.referenceHint,
  } satisfies HomeActionSelectorModel;
}

function fieldSelector(input: {
  entityType: string;
  fields: string[];
  acceptsCanonicalId?: boolean;
  acceptsHumanRef?: boolean;
  referenceHint: string;
}) {
  return {
    entityType: input.entityType,
    scope: ACTIVE_COMPANY_HOME_TOOL_SCOPE,
    fields: input.fields,
    acceptsCanonicalId: input.acceptsCanonicalId ?? false,
    acceptsHumanRef: input.acceptsHumanRef ?? true,
    referenceHint: input.referenceHint,
  } satisfies HomeActionSelectorModel;
}

function catalogEntry(
  input: Omit<HomeActionCatalogEntry, "registryKey" | "companyScope" | "enabled"> & {
    enabled?: boolean;
  },
): HomeActionCatalogEntry {
  return {
    ...input,
    registryKey: `internal.${input.name}` as `internal.${string}`,
    companyScope: ACTIVE_COMPANY_HOME_TOOL_SCOPE,
    enabled: input.enabled ?? true,
  };
}

type CatalogEntryBuilderInput = Omit<HomeActionCatalogEntry, "registryKey" | "companyScope" | "enabled" | "operationKind"> & {
  enabled?: boolean;
};

function readCatalogEntry(input: CatalogEntryBuilderInput) {
  return catalogEntry({
    ...input,
    operationKind: "read",
  });
}

function writeCatalogEntry(input: CatalogEntryBuilderInput) {
  return catalogEntry({
    ...input,
    operationKind: "write",
  });
}

function destructiveCatalogEntry(input: CatalogEntryBuilderInput) {
  return catalogEntry({
    ...input,
    operationKind: "destructive",
  });
}

